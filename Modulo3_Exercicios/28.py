#Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos três valores lidos
numero1 = float(input('Insira um número: '))
numero2 = float(input('Insira outro número: '))
numero3 = float(input('Insira mais um número: '))
soma = (numero1**2) + (numero2**2) + (numero3**2)
print(f'A soma dos quadrados dos números é: {soma}')